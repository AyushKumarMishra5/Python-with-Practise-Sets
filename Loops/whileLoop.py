# while loop:- the block keeps executing until the condition is true.
# Quiz : write a program to print numbers from 1-50 using while loop

i = 1
while (i<=50):
    print("The number is: ", i)
    i +=1
    
# quiz2: print the contents of the list using while loop
fruits = ["apple", "banana", "cherry", "date", "elderberry"]
i = 0
while(i<len(fruits)):
    print(fruits[i])
    i +=1